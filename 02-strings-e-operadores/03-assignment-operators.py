balance = 500

print(balance)

balance = 200

print(balance)

balance += 100  # Add 100 to balance
print(balance)  # Now balance is 300

balance -= 50   # Subtract 50 from balance
print(balance)  # Now balance is 250

balance /= 2    # Divide balance by 2
print(balance)  # Now balance is 125.0

balance //= 2   # Floor divide balance by 2
print(balance)  # Now balance is 62.0

balance *= 3    # Multiply balance by 3
print(balance)  # Now balance is 186.0

balance **= 2   # Exponentiate balance by 2
print(balance)  # Now balance is 34596.0